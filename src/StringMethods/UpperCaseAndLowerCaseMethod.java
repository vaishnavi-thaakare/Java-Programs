package StringMethods;

public class UpperCaseAndLowerCaseMethod {
	public static void main(String[] args) {
		String flower="LILI";
		String playerName=" Rahul";
		String modifiedPlayerName=playerName.toUpperCase();
		String modifiedString=flower.toLowerCase();		
		System.out.println("The lower case  version "+ flower + " is "+ modifiedString);
		System.out.println(" The upper case of  "+ playerName + " is  "+ modifiedPlayerName );
		
	}

}

